    <h1>Grow A Row Market Booth Ambassador</h1>
    <h3>Program Description</h3>
    <p>Grow A Row (GAR) encourages farmers, gardeners and local workplaces to grow an extra row of produce for delivery to 40 local agencies serving some of the over 20,000 people in the Kingston Area who live below the poverty line.  In 2017, Loving Spoonful collected 30,000 lbs of fresh produce that was delivered to the community.</p>
    <p>Grow A Row Program is one of several programs that directly support Loving Spoonful’s goals of enhancing access to healthy food for all; engaging diverse sectors of the community; and delivering innovative and impactful programming.</p>
    <h3>Position Purpose &ndash; Your impact</h3>
    <p>As the critical point of connection between the food donors and the service agencies, Grow a Row Market Booth Representatives are in charge of running the Loving Spoonful Booth at the Memorial Centre Farmers’ Market and the Downtown Kingston Public Market. These volunteers collect, weigh, and record donated produce.</p>
    <p>Grow A Row Market Booth Representatives are also the face of Loving Spoonful in the public. As they engage with curious passers-by, they help to raise awareness about what Loving Spoonful is and what we do.</p>
    <h3>Qualifications/Resources &ndash; What you&rsquo;ll need</h3>
    <p>Volunteers who have the most impact and enjoy this position most:</p>
    <ul>
    <li>Are outgoing, or willing to talk to strangers;</li>
    <li>Able to do some light physical labour and occasional heavy lifting; and</li>
    <li>Are able to work as part of a team.
    <li>Have online access on computer or mobile device to view and sign up for shifts. (Accommodation provided upon request.)</li>
    </ul>
    <h3>Key Duties and Responsibilities</h3>
    <p>Grow A Row Market Booth Ambassadors work in teams to run the Loving Spoonful Booths at the Memorial Centre Farmers’ Market and the Downtown Kingston Public Market.</p>
    <p><u>Assemble and/or pack-up tabling materials, table & canopy</u></p>
    <ul>
    <li>Arrive 5-10 minutes before your shift starts</li>
    <li>Gather the materials listed in the inventory and set up the booth.</li>
    <li>Inventory list and location-specific instructions will be provided at Orientation.</li>
    </ul>
    <p><u>Tell people about Loving Spoonful’s mission and programming</u></p>
    <ul>
    <li>Greet curious passers-by, answering their questions, offering info about Loving Spoonful, and inviting them to check out our materials, and sign up for our newsletter or to volunteer</li>
    <li>Encourage people to participate in activities offered at the table, take promotional materials & sign-up for our E-Newsletter</li>
    </ul>
    <p><u>Collect fresh produce and Submit reports</u></p>
    <ul>
    <li>Greet and thank donors</li>
    <li>Report details of donation (donor name, donor type, each produce type, produce weight) directly into the electronic track sheet (a tablet is provided)</li>
    </ul>
    <p><u>End of Shift</u></p>  
    <ul>
    <li>Calculate the total weight of donated produce</li>
    <li>Give the produce and total weight to the driver, and help load produce into the Driver’s vehicle</li>
    <li>Return materials to storage.</li>
    <li>Every other Saturday (DKPM only): use the Allen key in the tent repair kit to tighten every nut in the tent to ensure the joints don’t loosen and bust apart.</li>
    </ul>
    <p><u>Other responsibilities</u></p>
    <ul>
    <li>Take pictures of donated produce, and if able, circulate on social media, or send to supervisor</li>
    <li>Contact the program coordinator or <a href="mailto:food@lovingspoonful.org">food@lovingspoonful.org</a> if anyone signs up for the newsletter (send a photo of the sheet), makes a donation (arrange for pickup of funds), if anything is missing or damaged, or if you need help of any kind.</li>
    <li>Sign up for shifts on the online Love Bug</li>
    <li>Cancel shifts on the online Love Bug at least 3 days in advance if going to be absent, or directly inform the Loving Spoonful staff as soon as possible if last minute changes occur.</li>
    </ul>
    <h3>Time Commitments and Locations</h3>
    <p>From June to October, Market Booth Ambassadors cover shifts at 1 of 2 locations. Shifts are 2 to 2.5 hours long.</p>
    <p>Volunteers sign up for a particular booth team, and can volunteer weekly or once or twice per month. Booth teams are:</p>
    <ul>
    <li>Saturday (1:00PM – 3:00PM) at Downtown Kingston Public Market</li>
    <li>Saturday (3:00PM - 5:00PM) at Downtown Kingston Public Market</li>
    <li>Sunday (9:00AM – 12:00PM) at Memorial Centre Farmers Market</li>
    <li>Sunday (12:00PM - 2:30PM) at Memorial Centre Farmers Market</li>
    </ul>
    <p>Volunteers sign up for shifts through their own account (provided) on Loving Spoonful’s online Love Bug.</p>
    <p>Commitment is encouraged throughout the season; vacations are accommodated.</p>
