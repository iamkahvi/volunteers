    <h1>Gleaners</h1>
    <h3>Program Description</h3>
    <p>Working with local farmers, youth, and community volunteers, the Gleaning Project harvests surplus crops and delivers the produce to 40+ shelters, meal programs, and Fresh Food Market Stands.</p> 
    <p>Gleaning is one of several programs that directly support Loving Spoonful’s goals of enhancing access to healthy food for all; engaging diverse sectors of the community; and delivering innovative and impactful programming.</p>
    <h3>Position Purpose &ndash; Your impact</h3>
    <p>Gleaning volunteers have the key role of collecting surplus crops from farmers' fields after they have been commercially harvested or on fields where it is not economically profitable to harvest. Gleaners reduce food waste and directly help redistribute food to Kingston communities who need it.</p>
