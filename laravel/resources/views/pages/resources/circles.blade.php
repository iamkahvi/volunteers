    <h1>Position Title: Circles Program Assistants</h1>
    <h3>Program Description</h3>
    <p>Circles&reg; is a proven poverty reduction program, and is offered by Loving Spoonful, in partnership with Kingston Community Health Centres (KCHC) and the City of Kingston. The weekly gatherings around meals build community across class lines, teach food skills, and support people experiencing poverty toward their goals for self-sufficiency.</p>
    <p>Community Kitchens is one of several programs that directly support Loving Spoonful&rsquo;s goals of enhancing access to healthy food for all; engaging diverse sectors of the community; and delivering innovative and impactful programming.</p>
    <h3>Position Purpose &ndash; Your impact</h3>
    <p>Creating a space where participants can build food literacy, skills, confidence and community around good food, Circles&reg; Program Assistants are critical in helping to ensure that the cooking classes run smoothly and are positive experiences for vulnerable adults. They provide extra hands, eyes and words of support and encouragement.</p>
