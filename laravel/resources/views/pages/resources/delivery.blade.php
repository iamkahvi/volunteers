    <h3>SAFE LIFTING TIPS</h3>
    <p>This volunteer role may occassionally require lifting heavy objects. Improper lifting techniques can potentially lead to various back injuries. Here are a few simple tips about how to lift heavy objects safely:</p>
    <ul>
    <li>Always begin with your feet shoulder-width apart, with one foot slightly ahead of the other.</li>
    <li>Squat down, bending at the hips and knees only. Do not attempt to lift by bending forward.</li>
    <li>Look straight ahead, and keep your back straight, your chest out, and your shoulders back. This helps keep your upper back straight while having a slight arch in your lower back.</li>
    <li>Slowly lift by straightening your hips and knees (not your back). Keep your back straight.</li>
    <li>Hold the load as close to your body as possible, at the level of your belly button. Never lift a heavy object above shoulder level.</li>
    <li>Be careful not to twist your body as you lift or hold a heavy object. Use your feet to change direction, taking small steps.</li>
    <li>Lead with your hips as you change direction. Keep your shoulders in line with your hips as you move.</li>
    <li>Set down your load carefully, squatting with the knees and hips only.</li>
    </ul>
    <h3>SAFE DRIVING</h3>
    <p>When picking up or dropping off donations, always remember these tips:</p>
    <ul>
    <li>Some of our pickups and drop offs take place in locations where parking can be difficult. If stopping in a no parking zone, indicate that you will only be there temporarily, make sure to leave your four way lights on. Make sure you return to your vehicle as quickly as possible</li>
    <li>Make sure to keep your Loving Spoonful magnets displayed at all times.</li>
    <li>In the winter, the cold weather can make the magnets stiff, causing them to fall off. It may be best to leave them on your dashboard instead.</li>
    <li>Always obey all traffic rules.</li>
    <li>Never put speed or efficiency above your own safety, even if you are running late.</li>
    </ul>
