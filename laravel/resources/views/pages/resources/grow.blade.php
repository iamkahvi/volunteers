    <h1>GROW Project Workshop Assistant</h1>
    <h3>Program Description</h3>
    <p>Loving Spoonful's school-garden based GROW Project teaches over 600 students at 17 Kingston and area schools about good food, community, and collaboration. In each school, students in grades 4 to 8 participate in seven curriculum-connected classroom workshops that complement their garden time, teaching students about food systems, social justice and food security, food and culture, and healthy gardens and cooking. Visits from local farmers and nature-based field trips further assist students to make connections between their food, their health, their community, and the Earth.</p>
    <p>The GROW Project is one of several programs that directly support Loving Spoonful’s goals of enhancing access to healthy food for all; engaging diverse sectors of the community; and delivering innovative and impactful programming.
    <h3>Position Purpose &ndash; Your impact</h3>
    <p>GROW Project Workshop Assistants help out in hands-on workshops where students design, plant, care for and harvest vegetables in their school gardens while learning values of stewardship and responsibility.</p>
    <h3>Qualifications/Resources &ndash; What you&rsquo;ll need</h3>
    <ul>
    <li>A Criminal Record Check with ‘no criminal record’ is required. Cost is reimbursed; just provide your receipt.</li>
    <li>Ability to lead small groups (e.g. chopping vegetables, sowing seeds, looking at vermi-composting worms); a staff person from Loving Spoonful will always be present.</li>
    <li>Knowledge of basic gardening techniques such as “planting”, “weeding”, “thinning” and “harvesting” would be helpful for spring workshops, but is not required.</li>
    <li>Knowledge of basic food preparation techniques (chopping, mixing, using different ingredients) would be helpful but is not required.</li>
    <li>Light physical labour is required for some of the workshops.</li>
    <li>Online access on computer or mobile device to view and sign up for shifts is an asset. (Accommodation provided if needed.)</li>
    </ul>
    <h3>Key Duties and Responsibilities</h3>
    <p>Workshop Assistants help in the classroom and/or garden with students from grades 4 to 8. Their role could include:</p>
    <ul>
    <li>Helping students to prepare food – salsa, burritos, smoothies, or home-grown salad (fall & spring)</li>
    <li>Helping students discuss barriers & solutions to healthy eating (fall)</li>
    <li>Assisting students as they design and plant their garden (spring)</li>
    <li>Assisting students as they transplant tomato seedlings and explore vermi-composting (spring)</li>
    <li>In each workshop: Helping with set up, clean-up and interacting with students</li>
    </ul>
    <p>Specific volunteer position details will be available for each workshop opportunity so volunteers can choose the positions that are right for them.</p>
    <p><u>Other responsibilities</u></p>
    <ul>
    <li>Sign up for shifts on the online Volunteer Robot</li>
    <li>Cancel shifts on the online Volunteer Robot at least 3 days in advance if going to be absent, or directly inform the Loving Spoonful staff as soon as possible if last minute changes occur.</li>
    </ul>
    <h3>Time Commitments and Locations</h3>
    <p>Over the school year, the GROW Project delivers 7 workshops to each of 17 different schools in the City of Kingston. GROW Workshop Assistants are needed for 5 of these workshops (85 workshops in total) which take place in: late Sept/early Oct; November; April; May; and June.</p>
    <p>All scheduled GROW Workshops are posted on Loving Spoonful’s online Volunteer Robot, and each GROW Workshop Assistant will be provided an online account to sign up to volunteer for any workshop. Workshop shifts can occur any week day, Monday to Friday, during school hours, and are approximately 2-3 hours long.</p>
