    <h1>Community Kitchens Workshop Assistant</h1>
    <h3>Program Description</h3>
    <p>Loving Spoonful’s Community Kitchens are designed to get people together around good food. We cook with people of all ages, backgrounds, and abilities – children, youth, adults, families, newcomers, Indigenous community members, people with physical and intellectual differences, expert home cooks, and beginners. We get together to make healthy, affordable, and simple recipes in kitchens throughout Kingston. Hands-on learning and discussions of food topics helps build knowledge, skills, and confidence, encouraging participants to make healthy and informed food choices in the kitchen and beyond. In addition to food literacy development, our community kitchens are places where people socialize, building community connections and friendships around food. They also provide good food access as surplus food, healthy recipes, spices, and cookware are sent home to assist with cooking after programs are complete.</p>
    <p>Community Kitchens is one of several programs that directly support Loving Spoonful’s goals of enhancing access to healthy food for all; engaging diverse sectors of the community; and delivering innovative and impactful programming.
    <h3>Position Purpose &ndash; Your impact</h3>
    <p>Creating a space where participants can build food literacy, skills, confidence, and community around good food, Community Kitchen Workshop Assistants are critical in helping to ensure that the cooking classes run smoothly and are positive experiences for all participants.</p>
    <h3>Qualifications/Resources &ndash; What you&rsquo;ll need</h3>
    <ul>
    <li>A non-judgemental and respectful approach is always required when working with cooking participants, ensuring participants feel welcome, included, and dignified</li>   
    <li>Knowledge of basic gardening techniques such as “planting”, “weeding”, “thinning” and “harvesting” would be helpful for spring workshops, but is not required.</li>
    <li>Some cooking skills/knowledge</li>
    <li>An outgoing attitude and an ability to engage with others</li>
    <li>Be comfortable standing for long periods of time (1.5 – 3 hrs depending on the program)</li>
    <li>Light physical labour is required for some of the workshops.</li>
    <li>A Criminal Record Check is required, as program participants can bring their children to the classes. Cost is reimbursed; just provide your receipt.</li>
    <li>Online access on computer or mobile device to view and sign up for shifts is an asset. (Accommodation provided if needed.)</li>
    </ul>
    <h3>Key Duties and Responsibilities</h3>
    <p><u>On-site Duties</u></p>
    <ul>
    <li>Assist in leading a small group of participants through the workshop recipes, allowing participants to take the lead on making the meal and demonstrating specific skills/tips as required.</li>
    <li>Engage with participants in a fun and encouraging way while ensuring kitchen safety.</li>
    <li>Help with set up, clean up, dishes, and workshop activities required.</li>
    </ul>
    <p><u>Other responsibilities</u></p>
    <ul>
    <li>Sign up for shifts on the online Love Bug</li>
    <li>Cancel shifts on the online Love Bug at least 3 days in advance if going to be absent, or directly inform Loving Spoonful staff as soon as possible if last minute changes occur.</li>
    </ul>
    <h3>Time Commitments and Locations</h3>
    <ul>
    <li>Community Kitchen classes are offered as a series or one-time workshops. Programs specify: theme/Focus, location, number of classes (from 1 to 9), frequency of classes (single/one-off, weekly or monthly), length of classes (from 2 to 4 hours), time of classes, and number of volunteer positions needed.</li>
    <li>Volunteer opportunities vary throughout the year according to what programs are running.</li>
    <li>Shifts can be during the day, evening, or weekends</li>
    <li>It is preferred that a volunteer commit to a whole program as the continuity of their presence is enriching to participants. One-off/single-class programs are also offered.</li>
    <li>All scheduled Community Kitchens Classes are posted on Loving Spoonful’s online Love Bug, and each Workshop Facilitator will be provided an online account to sign up to volunteer for any class that has an unfilled volunteer shift.</li>
    </ul>
    <h3>Support &ndash; What we&rsquo;ll provide</h3>
    <ul>
    <li>A training meeting will be set up as at the start of each cooking program to review details about the cooking participants and volunteer position, as well as the recipes and activities that will be used during the workshops. This meeting will either take place before the program begins or directly before the first shift at the kitchen.</li>
    <li>The Community Kitchen Instructor will provide on-going support. Supervision will take place to make sure recipes are being followed (within reason) and people are having fun. </li>
    <li>Informal post-class debriefings with the Community Kitchen Instructor allows Workshop Assistants to discuss what is going well with the program, areas for improvement, as well as voice any suggestions, comments or concerns.</li>
    <li>An account with Loving Spoonful’s Love Bug to view and sign up for shifts</li>
    </ul>
